
public abstract class MyAbstract {
    int a;
    boolean b;

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public boolean isB() {
        return b;
    }

    public void setB(boolean b) {
        this.b = b;
    }

    public abstract String getMyString();
}
